package com.retrospective.model;

public class Sticker {

	private int id;
	
	private String comment;
	
	private String username;
	
	private String userId;
	
	private Double glad;
	
	private Double noControl;
	
	private int sessionCode;
	
	private String sessionToken;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Double getGlad() {
		return glad;
	}

	public void setGlad(Double glad) {
		this.glad = glad;
	}

	public Double getNoControl() {
		return noControl;
	}

	public void setNoControl(Double noControl) {
		this.noControl = noControl;
	}

	public int getSessionCode() {
		return sessionCode;
	}

	public void setSessionCode(int sessionCode) {
		this.sessionCode = sessionCode;
	}

	public String getSessionToken() {
		return sessionToken;
	}

	public void setSessionToken(String sessionToken) {
		this.sessionToken = sessionToken;
	}
	
	
	
}
